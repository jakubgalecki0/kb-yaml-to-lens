def _out_path(src_path: Path) -> Path:
    """Map source view.py → output .py path."""
    rel = src_path.relative_to(CORE_SRC)
    return OUT_DIR / rel


def _write_init(path: Path) -> None:
    """Ensure __init__.py exists at path (only if it doesn't already)."""
    if not path.exists():
        path.write_text(GENERATED_HEADER + '\n')


def _write_shared_view(out_dir: Path) -> None:
    """Write the hand-crafted shared/view.py."""
    shared_dir = out_dir / 'shared'
    shared_dir.mkdir(parents=True, exist_ok=True)
    _write_init(shared_dir / '__init__.py')
    (shared_dir / 'view.py').write_text(SHARED_VIEW_CONTENT)


