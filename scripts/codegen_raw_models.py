def _decorator_name(dec: ast.expr) -> str:
    """Return the base name of a decorator (handles Name, Attribute, and Call forms)."""
    if isinstance(dec, ast.Call):
        return _decorator_name(dec.func)
    if isinstance(dec, ast.Name):
        return dec.id
    if isinstance(dec, ast.Attribute):
        return dec.attr
    return ''


def _is_field_required(value: ast.expr) -> bool:
    """Return True if value is a `Field(...)` call with no default.

    Matches:
    - `Field(...)` — positional ellipsis
    - `Field(alias='...')` (or other keywords) with no `default=` or `default_factory=`
    """
    if not isinstance(value, ast.Call):
        return False
    func = value.func
    is_field = (isinstance(func, ast.Name) and func.id == 'Field') or (
        isinstance(func, ast.Attribute) and func.attr == 'Field'
    )
    if not is_field:
        return False
    # Positional ellipsis as first arg.
    if value.args and isinstance(value.args[0], ast.Constant) and value.args[0].value is ...:
        return True
    # No positional args and no default/default_factory keyword → required.
    if not value.args:
        kw_names = {kw.arg for kw in value.keywords}
        if 'default' not in kw_names and 'default_factory' not in kw_names:
            return True
    return False


def _make_optional(annotation: ast.expr) -> ast.expr:
    """Return `annotation | None` unless already optional."""
    if _annotation_has_none(annotation):
        return annotation
    return ast.BinOp(left=annotation, op=ast.BitOr(), right=ast.Constant(value=None))


def _annotation_has_none(annotation: ast.expr) -> bool:
    """Return True if the annotation already includes None."""
    if isinstance(annotation, ast.Constant) and annotation.value is None:
        return True
    if isinstance(annotation, ast.BinOp) and isinstance(annotation.op, ast.BitOr):
        return _annotation_has_none(annotation.left) or _annotation_has_none(annotation.right)
    if isinstance(annotation, ast.Subscript):
        # Handle Optional[X]
        if isinstance(annotation.value, ast.Name) and annotation.value.id == 'Optional':
            return True
    return False


def _field_with_default_none(value: ast.expr) -> ast.expr:
    """Convert Field(...) to Field(default=None) by removing the positional ellipsis arg."""
    assert isinstance(value, ast.Call)
    new_args = [a for a in value.args if not (isinstance(a, ast.Constant) and a.value is ...)]
    new_keywords = [kw for kw in value.keywords if kw.arg != 'default']
    new_keywords.insert(0, ast.keyword(arg='default', value=ast.Constant(value=None)))
    return ast.Call(func=value.func, args=new_args, keywords=new_keywords)



def _relax_literal(annotation: ast.expr) -> ast.expr:
    """Replace Literal[...] type annotations with their base Python type.

    Literal types in decompile raw models are too strict for real-world Kibana data —
    e.g. a `terms` column may have dataType='ip' rather than the modelled 'string'.
    We relax all Literal[...] to their equivalent base type so validation never rejects
    otherwise-valid Kibana data on discriminator fields.

    Mapping:
    - Literal[str_values...]  → str
    - Literal[bool_value]     → bool   (True/False before int check)
    - Literal[int_values...]  → int

    The annotation is walked recursively so ``Literal['x'] | None`` becomes ``str | None``.
    """
    # Literal[...] subscript
    if isinstance(annotation, ast.Subscript) and isinstance(annotation.value, ast.Name) and annotation.value.id == 'Literal':
        elts: list[ast.expr] = []
        slice_node = annotation.slice
        if isinstance(slice_node, ast.Tuple):
            elts = list(slice_node.elts)
        else:
            elts = [slice_node]
        # Infer base type from the constant values
        values = [e.value for e in elts if isinstance(e, ast.Constant)]
        if values:
            if any(isinstance(v, bool) for v in values):
                return ast.Name(id='bool', ctx=ast.Load())
            if all(isinstance(v, str) for v in values):
                return ast.Name(id='str', ctx=ast.Load())
            if all(isinstance(v, int) for v in values):
                return ast.Name(id='int', ctx=ast.Load())
        # Fallback: keep as-is
        return annotation

    # Walk into BinOp (union types like `Literal['x'] | None`).
    if isinstance(annotation, ast.BinOp) and isinstance(annotation.op, ast.BitOr):
        annotation.left = _relax_literal(annotation.left)
        annotation.right = _relax_literal(annotation.right)
        return annotation

    # Walk into Subscript slices (e.g., Annotated[Literal['x'], ...]).
    if isinstance(annotation, ast.Subscript):
        annotation.slice = _relax_literal(annotation.slice)
        return annotation

    # Walk into Tuple elements.
    if isinstance(annotation, ast.Tuple):
        annotation.elts = [_relax_literal(e) for e in annotation.elts]
        return annotation

    return annotation


def _replace_complex_unions(annotation: ast.expr, transformer: '_RawModelTransformer') -> ast.expr:
    """Replace complex union type annotations with Any.

    Targets:
    - KbnControlTypes                 → Any
    - KbnLensColumnTypes              → Any
    - KbnESQLColumnTypes              → Any
    """
    # KbnControlTypes / KbnLensColumnTypes / KbnESQLColumnTypes → Any
    if isinstance(annotation, ast.Name) and annotation.id in {'KbnControlTypes', 'KbnLensColumnTypes', 'KbnESQLColumnTypes'}:
        transformer.any_introduced = True
        return ast.Name(id='Any', ctx=ast.Load())

    # Walk into BinOp (union types like `X | Y | None`).
    if isinstance(annotation, ast.BinOp) and isinstance(annotation.op, ast.BitOr):
        annotation.left = _replace_complex_unions(annotation.left, transformer)
        annotation.right = _replace_complex_unions(annotation.right, transformer)
        return annotation

    # Walk into Subscript slices (e.g., Annotated[X, ...], list[X]).
    if isinstance(annotation, ast.Subscript):
        annotation.slice = _replace_complex_unions(annotation.slice, transformer)
        return annotation

    # Walk into Tuple elements (e.g., Annotated[X, Y, Z]).
    if isinstance(annotation, ast.Tuple):
        annotation.elts = [_replace_complex_unions(e, transformer) for e in annotation.elts]
        return annotation

    return annotation


def _build_list_dict_filter_validator(field_name: str) -> list[ast.stmt]:
    """Build AST nodes for a field_validator that drops non-dict items from a list field."""
    src = textwrap.dedent(f"""\
        @field_validator('{field_name}', mode='before')
        @classmethod
        def _filter_{field_name}_non_dicts(cls, v: object) -> object:
            if isinstance(v, list):
                from typing import cast as _cast
                return _cast(list[object], [item for item in _cast(list[object], v) if isinstance(item, dict)])
            return v
    """)
    tree = ast.parse(src)
    return tree.body


def _build_combined_json_and_filter_validator(field_name: str) -> list[ast.stmt]:
    """Build a single validator that JSON-parses a string then filters non-dict items.

    Used when a field is in both JSON_STRING_FIELDS and LIST_DICT_FILTER_FIELDS.
    A single validator avoids pydantic v2's reverse-order execution of multiple
    mode='before' validators on the same field.
    """
    src = textwrap.dedent(f"""\
        @field_validator('{field_name}', mode='before')
        @classmethod
        def _parse_and_filter_{field_name}(cls, v: object) -> object:
            if isinstance(v, str):
                import json as _json
                from typing import cast as _cast
                v = _cast(object, _json.loads(v))
            if isinstance(v, list):
                from typing import cast as _cast
                return _cast(list[object], [item for item in _cast(list[object], v) if isinstance(item, dict)])
            return v
    """)
    tree = ast.parse(src)
    return tree.body


def _build_json_validator(field_name: str) -> list[ast.stmt]:
    """Build AST nodes for a field_validator that parses a JSON string field."""
    src = textwrap.dedent(f"""\
        @field_validator('{field_name}', mode='before')
        @classmethod
        def _parse_{field_name}_json(cls, v: object) -> object:
            if isinstance(v, str):
                from typing import cast as _cast
                import json as _json
                return _cast(object, _json.loads(v))
            return v
    """)
    tree = ast.parse(src)
    return tree.body


