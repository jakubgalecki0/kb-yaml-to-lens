def _compute_relative_module(src_path: Path) -> str:
    """Return the dotted module path relative to kb_dashboard_core root."""
    rel = src_path.relative_to(CORE_SRC)
    parts = list(rel.parts)
    parts[-1] = parts[-1].removesuffix('.py')
    return '.'.join(parts)


def _transform_source(source: str, src_path: Path) -> str:
    """Full AST-based transformation of one source view.py → raw model file content."""
    tree = ast.parse(source)

    # Only process files that define Kbn* classes at the top level.
    has_kbn_classes = any(
        isinstance(node, ast.ClassDef) and node.name.startswith('Kbn')
        for node in tree.body
    )
    if not has_kbn_classes:
        return ''

    # Drop the source module docstring (if any) — we'll prepend our own header.
    if (
        tree.body
        and isinstance(tree.body[0], ast.Expr)
        and isinstance(tree.body[0].value, ast.Constant)
        and isinstance(tree.body[0].value.value, str)
    ):
        tree.body.pop(0)

    transformer = _RawModelTransformer()
    transformed = transformer.visit(tree)
    ast.fix_missing_locations(transformed)

    # Override panelsJSON annotation to concrete panel union after transformation.
    _override_panels_json_annotation(transformed)

    # Add validation_alias where only serialization_alias exists.
    _add_validation_aliases(transformed)

    if transformer.any_introduced:
        _ensure_import(transformed, 'typing', 'Any')
    if transformer.needs_field_validator:
        _ensure_import(transformed, 'pydantic', 'field_validator')

    # Insert BaseRawVwModel import after the last existing import.
    base_import = ast.parse(BASE_CLASS_IMPORT).body[0]
    insert_pos = 0
    for i, node in enumerate(transformed.body):
        if isinstance(node, (ast.Import, ast.ImportFrom)):
            insert_pos = i + 1
    transformed.body.insert(insert_pos, base_import)
    ast.fix_missing_locations(transformed)

    # Unparse back to source.
    code = ast.unparse(transformed)

    header = (
        GENERATED_HEADER
        + '"""Permissive view models generated from kb_dashboard_core view models."""\n\n'
    )
    return header + code + '\n'


