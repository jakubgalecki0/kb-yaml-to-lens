SHARED_VIEW_CONTENT = GENERATED_HEADER + textwrap.dedent("""\
    \"\"\"Permissive base model and shared types for kbn_raw_models.\"\"\"

    from typing import ClassVar

    from pydantic import BaseModel as PydanticBaseModel
    from pydantic import ConfigDict

    from kb_dashboard_core.shared.view import KbnReference, OmitIfNone, RootDict  # re-exports for generated modules

    __all__ = ['BaseRawVwModel', 'KbnReference', 'OmitIfNone', 'RootDict']


    class BaseRawVwModel(PydanticBaseModel):
        \"\"\"Permissive base for raw Kibana view models — extra fields are ignored, strict mode off.\"\"\"

        model_config: ClassVar[ConfigDict] = ConfigDict(
            strict=False,
            extra='ignore',
            frozen=False,
            populate_by_name=True,
            use_enum_values=True,
        )
""")


# ---------------------------------------------------------------------------
# File-writing helpers
# ---------------------------------------------------------------------------


def _out_path(src_path: Path) -> Path:
    """Map source view.py → output .py path."""
    rel = src_path.relative_to(CORE_SRC)
    return OUT_DIR / rel


def _write_init(path: Path) -> None:
    """Ensure __init__.py exists at path (only if it doesn't already)."""
    if not path.exists():
        path.write_text(GENERATED_HEADER + '\n')


def _write_shared_view(out_dir: Path) -> None:
    """Write the hand-crafted shared/view.py."""
    shared_dir = out_dir / 'shared'
    shared_dir.mkdir(parents=True, exist_ok=True)
    _write_init(shared_dir / '__init__.py')
    (shared_dir / 'view.py').write_text(SHARED_VIEW_CONTENT)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def main() -> None:
    """Generate all raw model files."""
    if OUT_DIR.exists():
        shutil.rmtree(OUT_DIR)
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    generated: list[Path] = []

    # Write shared/view.py manually.
    _write_shared_view(OUT_DIR)

    for src_path in sorted(CORE_SRC.rglob('view.py')):
        if src_path.parent.name == 'shared':
            continue  # Handled manually above

        source = src_path.read_text()
        code = _transform_source(source, src_path)
        if not code:
            continue

        out = _out_path(src_path)
        out.parent.mkdir(parents=True, exist_ok=True)

        # Ensure __init__.py exists at every package level between OUT_DIR and out.parent.
        pkg_dir = out.parent
        while pkg_dir != OUT_DIR.parent:
            _write_init(pkg_dir / '__init__.py')
            pkg_dir = pkg_dir.parent

        out.write_text(code)
        generated.append(out)
        print(f'  wrote: {out.relative_to(REPO_ROOT)}')

    # Write top-level __init__.py that re-exports KbnDashboard for convenience.
    init_path = OUT_DIR / '__init__.py'
    init_path.write_text(
        GENERATED_HEADER
        + '"""Permissive view models for parsing real-world Kibana dashboard JSON."""\n\n'
        + 'from kb_dashboard_tools.decompile.kbn_raw_models.dashboard.view import KbnDashboard\n\n'
        + "__all__ = ['KbnDashboard']\n"
    )

    print(f'\nGenerated {len(generated)} files in {OUT_DIR.relative_to(REPO_ROOT)}')

    # Auto-format with ruff to ensure CI passes.
    tools_dir = REPO_ROOT / 'packages' / 'kb-dashboard-tools'

    ruff_check = subprocess.run(
        ['uv', 'run', 'ruff', 'check', '--fix', str(OUT_DIR)],
        cwd=tools_dir,
        capture_output=True,
        text=True,
    )
    if ruff_check.returncode != 0:
        raise SystemExit(f'ruff check --fix failed:\n{ruff_check.stdout[:1000]}{ruff_check.stderr[:1000]}')
    print('ruff check --fix: OK')

    ruff_result = subprocess.run(
        ['uv', 'run', 'ruff', 'format', str(OUT_DIR)],
        cwd=tools_dir,
        capture_output=True,
        text=True,
    )
    if ruff_result.returncode != 0:
        raise SystemExit(f'ruff format failed:\n{ruff_result.stdout[:1000]}{ruff_result.stderr[:1000]}')
    print('ruff format: OK')


if __name__ == '__main__':
    main()
