def _ensure_import(tree: ast.Module, module: str, name: str) -> None:
    """Ensure `name` is imported from `module` in the module AST."""
    for node in tree.body:
        if isinstance(node, ast.ImportFrom) and node.module == module:
            if name not in [a.name for a in node.names]:
                node.names.append(ast.alias(name=name))
            return
    insert_pos = max(
        (i + 1 for i, node in enumerate(tree.body) if isinstance(node, (ast.Import, ast.ImportFrom))),
        default=0,
    )
    tree.body.insert(insert_pos, ast.ImportFrom(module=module, names=[ast.alias(name=name)], level=0))


def _override_panels_json_annotation(tree: ast.Module) -> bool:
    """Replace panelsJSON annotation with a union of all concrete panel types.

    The strict model has `panelsJSON: list[KbnBasePanel]`. After transformation it becomes
    `list[KbnBasePanel] | None`. We replace the list element with a full union of concrete
    panel types (KbnLensPanel | KbnMarkdownPanel | ... | KbnBasePanel) so pydantic
    validates each panel into the correct typed class. KbnBasePanel is kept last as a
    catch-all for unknown panel types (esql, custom_viz, etc.).

    Returns True if imports were injected (always True when the annotation is found).
    """
    for node in ast.walk(tree):
        if not (
            isinstance(node, ast.AnnAssign)
            and isinstance(node.target, ast.Name)
            and node.target.id == 'panelsJSON'
            and 'KbnBasePanel' in ast.unparse(node.annotation)
        ):
            continue

        # Build union: KbnLensPanel | KbnMarkdownPanel | ... | KbnBasePanel
        all_names = [name for _, name in CONCRETE_PANEL_TYPES] + ['KbnBasePanel']
        union: ast.expr = ast.Name(id=all_names[0], ctx=ast.Load())
        for name in all_names[1:]:
            union = ast.BinOp(left=union, op=ast.BitOr(), right=ast.Name(id=name, ctx=ast.Load()))

        node.annotation = ast.BinOp(
            left=ast.Subscript(value=ast.Name(id='list', ctx=ast.Load()), slice=union, ctx=ast.Load()),
            op=ast.BitOr(),
            right=ast.Constant(value=None),
        )
        node.value = ast.Constant(value=None)

        # Inject imports for concrete panel types.
        insert_pos = 0
        for i, n in enumerate(tree.body):
            if isinstance(n, (ast.Import, ast.ImportFrom)):
                insert_pos = i + 1
        base_module = 'kb_dashboard_tools.decompile.kbn_raw_models.'
        for rel_module, class_name in reversed(CONCRETE_PANEL_TYPES):
            tree.body.insert(
                insert_pos,
                ast.ImportFrom(
                    module=base_module + rel_module,
                    names=[ast.alias(name=class_name)],
                    level=0,
                ),
            )
        return True
    return False


def _add_validation_aliases(tree: ast.Module) -> None:
    """For any Field with serialization_alias but no validation_alias, add validation_alias."""
    for node in ast.walk(tree):
        if not isinstance(node, ast.AnnAssign) or not isinstance(node.value, ast.Call):
            continue
        call = node.value
        kw_names = [kw.arg for kw in call.keywords]
        if 'serialization_alias' in kw_names and 'validation_alias' not in kw_names:
            for kw in call.keywords:
                if kw.arg == 'serialization_alias':
                    idx = call.keywords.index(kw)
                    call.keywords.insert(
                        idx,
                        ast.keyword(arg='validation_alias', value=kw.value),
                    )
                    break


