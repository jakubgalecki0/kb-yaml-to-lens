"""Codegen script: generate permissive raw view models from Kbn* source view.py files.

Uses Python AST to parse and transform kb_dashboard_core view.py files into permissive
equivalents (BaseRawVwModel base, all fields optional). Class names are kept exactly as-is.

Run with:
    uv run python scripts/codegen_raw_models.py  (from repo root, in kb-dashboard-tools venv)
    or:
    cd packages/kb-dashboard-tools && uv run python ../../scripts/codegen_raw_models.py

Output:
    packages/kb-dashboard-tools/src/kb_dashboard_tools/decompile/kbn_raw_models/
"""

import ast
import shutil
import subprocess
import textwrap
from pathlib import Path

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------

REPO_ROOT = Path(__file__).resolve().parent.parent
CORE_SRC = REPO_ROOT / 'packages' / 'kb-dashboard-core' / 'src' / 'kb_dashboard_core'
OUT_DIR = (
    REPO_ROOT
    / 'packages'
    / 'kb-dashboard-tools'
    / 'src'
    / 'kb_dashboard_tools'
    / 'decompile'
    / 'kbn_raw_models'
)

GENERATED_HEADER = '# AUTO-GENERATED by scripts/codegen_raw_models.py — DO NOT EDIT\n'

# Explicit list of source modules to generate (relative to CORE_SRC).
# Only these are needed by the decompiler — all other view.py files in kb_dashboard_core
# (panel subtypes, chart-type states, column types) are intentionally skipped because
# the decompiler works entirely with raw dicts and never imports those models.
GENERATED_SOURCE_MODULES: list[str] = [
    'dashboard/view.py',
    'filters/view.py',
    'panels/view.py',
    'controls/view.py',
    'queries/view.py',
]

# Fields that may contain non-dict items; inject a mode='before' validator to filter them.
LIST_DICT_FILTER_FIELDS = frozenset({
    'references',
    'panelsJSON',
})

# Concrete panel types that replace KbnBasePanel in the panelsJSON union (in priority order).
# Empty: all panels parse as KbnBasePanel — type routing is done on panel.type string, not isinstance.
# The generated panel-specific files (KbnLensPanel etc.) are produced but not imported.
CONCRETE_PANEL_TYPES: list[tuple[str, str]] = []

# Extra fields injected into specific generated classes (decompile-only concerns).
# These fields appear in Kibana panel JSON but are absent from the compile-side core models.
GENERATED_CLASS_EXTRA_FIELDS: dict[str, list[tuple[str, str]]] = {
    'KbnBasePanel': [
        ('type', 'str | None'),
        ('title', 'str | None'),
        ('description', 'str | None'),
        ('panelRefName', 'str | None'),
        # embeddableConfig is Any so pydantic does not validate the nested Kibana structure.
        # The decompiler works entirely with raw dicts after panel parsing.
        ('embeddableConfig', 'Any | None'),
    ],
}

# Fields that may arrive as JSON-encoded strings; inject a mode='before' validator.
JSON_STRING_FIELDS = frozenset({
    'panelsJSON',
    'optionsJSON',
    'searchSourceJSON',
    'ignoreParentSettingsJSON',
})

# Base classes from kb_dashboard_core that we replace with BaseRawVwModel.
REPLACED_BASE_CLASSES = frozenset({'BaseVwModel', 'BaseModel'})

# Import names we always drop — they come from shared/view.py (BaseRawVwModel, OmitIfNone, RootDict).
DROPPED_IMPORT_NAMES = frozenset({'BaseVwModel', 'BaseModel', 'OmitIfNone', 'RootDict'})

# Pydantic decorators whose methods should be dropped entirely.
DROP_DECORATORS = frozenset({'model_validator', 'field_serializer', 'model_serializer'})

# Pydantic names to drop from pydantic imports (no longer needed in raw models).
DROPPED_PYDANTIC_NAMES = frozenset({'field_serializer', 'model_serializer', 'model_validator',
                                    'SerializerFunctionWrapHandler'})

BASE_CLASS_IMPORT = (
    'from kb_dashboard_tools.decompile.kbn_raw_models.shared.view import BaseRawVwModel, OmitIfNone, RootDict'
)

